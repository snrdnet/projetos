     public void centralizar(JPanel obj) {  
        int pontoLargura = ((this.getWidth() / 2) - obj.getWidth() / 2) - 2;  
        int pontoAltura = ((this.getHeight() / 2) - obj.getHeight() / 2) - 16;  
          
        obj.setLocation(pontoLargura, pontoAltura);  
    }
     public void centralizarJFrame(JFrame obj) {  
        int pontoLargura = ((this.getWidth() / 2) - obj.getWidth() / 2) + 200;  
        int pontoAltura = ((this.getHeight() / 2) - obj.getHeight() / 2) - 16;  
          
        obj.setLocation(pontoLargura, pontoAltura);  
    }
